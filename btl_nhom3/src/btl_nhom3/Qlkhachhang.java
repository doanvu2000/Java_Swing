    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        KhachHang kh = new KhachHang();
        kh.setTenKH(txt_tenkh.getText());
        kh.setCMT(txt_cmt.getText());
        kh.setDiaChi(txt_dc.getText());
        kh.setSDTKH(txt_sdt.getText());
        String x = txt_makh.getText();
        String y = CBLoaiKH.getSelectedItem().toString();
        if (kh.getTenKH().isEmpty() || kh.getCMT().isEmpty() || kh.getDiaChi().isEmpty() || kh.getSDTKH().isEmpty() || x.isEmpty() || y.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int ma = Integer.parseInt(x);
        try {
            if (checkExist_MaKH(ma)) {
                JOptionPane.showMessageDialog(rootPane, "Khách hàng này đã có trong danh sách!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Qlkhachhang.class.getName()).log(Level.SEVERE, null, ex);
        }
        kh.setMaKH(ma);
        kh.setLoaiKH(y);
        if (conn.AddKH(kh)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thông tin thành công", "Add data successfull", JOptionPane.INFORMATION_MESSAGE);
            btn_resetActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thông tin thất bại", "Add data failed", JOptionPane.ERROR_MESSAGE);
        }
        show_new(kh);
    }//GEN-LAST:event_btnAddActionPerformed

