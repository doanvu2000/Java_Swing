    void hienThi() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) tableDonGia.getModel();
        model.setColumnIdentifiers(new Object[]{
            "Loại khách hàng","Đơn giá"
        });
        ResultSet rs = conn.stmt.executeQuery("select * from don_gia");
        while(rs.next()){
            model.addRow(new Object[]{
                rs.getString("LoaiKH"), rs.getInt("DonGia")
        });
        }
        
    }
    static void Show() throws SQLException {
        ArrayList<KhachHang> list_check = conn.getAllData();
        for (int i = 0; i < list_check.size(); i++) {
            KhachHang x = list_check.get(i);
            model.addRow(new Object[]{
                x.getMaKH(), x.getTenKH(), x.getCMT(), x.getDiaChi(), x.getSDTKH(), x.getLoaiKH()
            }
            );
        }
    }
    
    boolean checkExist_MaKH(int code) throws SQLException {
        ArrayList<KhachHang> list_check = conn.getAllData();
        for (int i = 0; i < list_check.size(); i++) {
            if (list_check.get(i).getMaKH() == code) {
                return true;
            }
        }
        return false;
    }
    
    static void Search_MaKH(int makh) throws SQLException {
        ArrayList<KhachHang> list_check = conn.search(makh);
        for (int i = 0; i < list_check.size(); i++) {
            KhachHang x = list_check.get(i);
            model.addRow(new Object[]{
                x.getMaKH(), x.getTenKH(), x.getCMT(), x.getDiaChi(), x.getSDTKH(), x.getLoaiKH()
            }
            );
        }
    }
    static void Show_By_Makh(int code) throws SQLException {
        ArrayList<KhachHang> list_check = conn.getAllData();
        for (int i = 0; i < list_check.size(); i++) {
            if (list_check.get(i).getMaKH() == code) {
                KhachHang x = list_check.get(i);
                model.addRow(new Object[]{
                    x.getMaKH(), x.getTenKH(), x.getCMT(), x.getDiaChi(), x.getSDTKH(), x.getLoaiKH()
                }
                );
            }
        }
    }

